function openBigNav() {
	document.getElementById("small-menu").style.display = "none";
	document.getElementById("big-menu").style.display = "block";
	document.getElementById("main-content").style.marginLeft = "260px";
}

function openSmallNav() {
	document.getElementById("small-menu").style.display = "block";
	document.getElementById("big-menu").style.display = "none";
	document.getElementById("main-content").style.marginLeft = "64px";
}